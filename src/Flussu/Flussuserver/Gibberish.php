  public function isGibberish($text){
    $res=$this->gibberishTest($text);
    if (is_bool($res))
      $ret=$res;
    else
      $ret=$res['is_gibberish'];
    if (!$ret){
        $ret=$this->badWordsTest($text);
    }
    return $ret;
  }

  public function badWordsTest($text){
    $isBad=false;
    $words=explode(" ",strtolower($text));
    foreach ($words as $word){
      if (!empty($word) && array_search ($word, $this->badwords))
        $isBad=true;
    }
    return $isBad;
  }

  public function gibberishTest($text){
    $gibber = new \Gibberish\GibberishDetector( $this->pretrained_gibberish_model );
    $result=$gibber->evaluate( $text, true );
    return $result;
  }

